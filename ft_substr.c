#include "libft.h"

char *ft_substr(char const *s, unsigned int start, size_t len)
{
	int	i;
	char	*str;
	i = 0;
	while (s[start + i] && i < len)
		 i++;
	str = malloc(sizeof(char) * i + 1);
	if (!str)
		return (NULL);
	i = 0;
	while (s[start + i] && i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	return (str);
}
